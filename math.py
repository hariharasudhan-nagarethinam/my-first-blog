class Math:

	def __init__(x, y):
		self.x = x
		self.y = y

	def add(self):
		if x is not None and y is not None:
			return self.x + self.y
		
	def sub(self):
		return self.x - self.y
		
	def mul(self):
		if x is not None and y is not None:
			return self.x * self.y
		
	def div(self):
		return self.x / self.y